void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
	if (hadc == ADC_NCDT_port){
		NCDT_port_scanCompleted = true;
	} else if (hadc == ADC_NCDT_star){
		NCDT_star_scanCompleted = true;
	}
}

